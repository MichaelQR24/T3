     public void agregarPersona(Persona persona){
        personass.add(persona);
        modelo.addElement(persona.getTexto());
        lstListar.setModel(modelo);
    }
     public void editarPersona(Persona persona){
       int index = lstListar.getSelectedIndex();
       personass.set(index, persona);
       modelo.set(index, persona.getTexto());
       lstListar.setModel(modelo);
   }
     public void editarPersonaBuscar(int index, Persona persona){
       personass.set(index, persona);
       modelo.set(index, persona.getTexto());
       lstListar.setModel(modelo);
   }
    
