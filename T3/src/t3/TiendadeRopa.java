    @Override
public String obtenerInformacion() {
    StringBuilder informacion = new StringBuilder();
    for (Persona persona : personass) {
        informacion.append("Nombre: ").append(persona.getNombre()).append("\n");
        informacion.append("Ropa: ").append(persona.getropa()).append("\n");
        informacion.append("Precio: ").append(persona.getprecio()).append(" soles").append("\n\n");
    }
    
    if (informacion.length() > 0) {
        informacion.deleteCharAt(informacion.length() - 1);
    } else {
        informacion.append("No hay información disponible.");
    }
    
    return informacion.toString();
}

