    private void actualizarListaInfo() {
    String informacion = obtenerInformacion();  // Llama al método para obtener información.
    DefaultListModel<String> modeloLista = new DefaultListModel<>();
    
    if (!informacion.isEmpty()) {
        String[] lineas = informacion.split("\n");
        for (String linea : lineas) {
            modeloLista.addElement(linea);
        }
    }
    
    lstListar.setModel(modeloLista);
}
